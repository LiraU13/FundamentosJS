// Operadores Matemáticos

console.log(1 + 1);
console.log(1 - 1);
console.log(1 * 1);
console.log(1 / 1);
console.log(1 % 1);
console.log(3 ** 3);

// Más operadores
let a = 10;

console.log(a);
a += 10; //a = a + 10
console.log(a);
a -= 10; //a = a - 10
console.log(a);
a *= 10; //a = a * 10
console.log(a);
a /= 10; //a = a / 10
console.log(a);
a %= 10; //a = a % 10
console.log(a);

// Operadores drelacionales
console.log(10 < 10);
console.log(10 <= 10);
console.log(10 > 10);
console.log(10 >= 10);
console.log(10 != 10);
console.log(10 === 10);

// Operadores lógicos
console.log(true && true);
console.log(true || true);
console.log(!true);
